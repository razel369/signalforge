export function cityToZone(city: string): Zone {
  const trimmed = city.trim();
  if (!trimmed || trimmed === "לא ידוע") return "מרכז";

  if (CITY_TO_ZONE[trimmed]) return CITY_TO_ZONE[trimmed];

  // Arabic city prefixes/suffixes → mostly in צפון or דרום
  if (trimmed.startsWith("אום אל") || trimmed.startsWith("עראבה") || trimmed.startsWith("טמרה") || trimmed.startsWith("סח'נין")) {
    return "צפון";
  }

  for (const [key, zone] of Object.entries(CITY_TO_ZONE)) {
    if (key.length >= 3 && trimmed.includes(key)) return zone;
  }

  return "מרכז";
}

